    private void btEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditarActionPerformed
        int opcao = li_receber.getSelectedRow();
        if (opcao >= 0) {
            Recebimento c = new Recebimento();
            c.setValor(Integer.parseInt(li_receber.getValueAt(opcao, 0).toString()));
            c.setParcela(Integer.parseInt(li_receber.getValueAt(opcao, 1).toString()));
            c.setFormaPag(li_receber.getValueAt(opcao, 2).toString());
            c.setData_venc(li_receber.getValueAt(opcao, 3).toString());
            c.setId_caixa_fk(Integer.parseInt(li_receber.getValueAt(opcao, 4).toString()));
            c.setId_venda_fk(Integer.parseInt(li_receber.getValueAt(opcao, 5).toString()));
            c.setId_recebimento(Integer.parseInt(li_receber.getValueAt(opcao, 6).toString()));
            c.setValor(Double.parseDouble(li_receber.getValueAt(opcao, 7).toString()));
            RecebimentoDAO cdao = new RecebimentoDAO();
            cdao.editarPorID(c);
            preencherTabela();
    }//GEN-LAST:event_btEditarActionPerformed
    }
    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        int opcao = li_receber.getSelectedRow();
        if (opcao >= 0) {
            Recebimento c = new Recebimento();
            c.setId_recebimento(Integer.parseInt(li_receber.getValueAt(opcao, 0).toString()));
            RecebimentoDAO cdao = new RecebimentoDAO();
            cdao.deletar(c);
            preencherTabela();
        }
    }//GEN-LAST:event_btExcluirActionPerformed

    public void gerarTabelaVendas() {
        DefaultTableModel modelo = (DefaultTableModel) li_receber.getModel();
        modelo.setNumRows(0);
        DespesaDAO pDAO = new DespesaDAO();
        List<Despesa> Lista = pDAO.listarTodos();
        for (Despesa p : Lista) {
            modelo.addRow(new Object[]{p.getId_despesa(), p.getValor(), p.getData(), p.getFormaPag(), p.getDesc()});
        }
    }

    public void gerarTabelaVendas_com_Consulta() {
        DefaultTableModel modelo = (DefaultTableModel) li_receber.getModel();
        modelo.setNumRows(0);
        DespesaDAO pDAO = new DespesaDAO();
        List<Despesa> Lista = pDAO.listarTodos();
        for (Despesa p : Lista) {
            if (((p.getData().toLowerCase()).contains(edID.getText().toLowerCase()))) {
                modelo.addRow(new Object[]{p.getId_despesa(), p.getValor(), p.getData(), p.getFormaPag(), p.getDesc()});
            }//        "Id", "Valor", "Data", "Hora", "Forma Pagamento", "Cliente", "Funcionário", "Produto", "Quantidade"
        }
    }
    
    public void preencherTabela(String text) {
        DespesaDAO cdao = new DespesaDAO();
        List<Despesa> lista = cdao.listarTodos();
        DefaultTableModel tabela = (DefaultTableModel) li_receber.getModel();
        tabela.setRowCount(0);
        for (Despesa c : lista) {
            tabela.addRow(new Object[]{c.getDesc(), c.getValor(),c.getData(), c.getFormaPag()});
        }
    }
    
