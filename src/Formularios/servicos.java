    private void btCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCadastrarActionPerformed
        Servico p = new Servico();
        ServicoDAO pDAO = new ServicoDAO();
        Usuario user = Usuario.getInstancia();
        //Pegando valores dos EDs
        p.setDesc(edDesc.getText());
        p.setTempo(edTempo.getText());
        p.setValor(Double.parseDouble(edValor.getText()));
        p.setId_funcionario_fk(user.getId());
        //Enviar para o DAO
        if(mode == 0){
            pDAO.cadastrarServico(p);
        }else{
            p.setId_servico(id_edit);
            pDAO.editarPorID(p);
            trocarModo(p);
        }
        limparCampos();
    }//GEN-LAST:event_btCadastrarActionPerformed

