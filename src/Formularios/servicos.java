    public void gerarTabela(){
        DefaultTableModel modelo = (DefaultTableModel) tabelaServicos.getModel();
        modelo.setNumRows(0);
        ServicoDAO pDAO = new ServicoDAO();
        List<Servico> Lista = pDAO.listarTodos();
        for(Servico p: Lista){     
            modelo.addRow(new Object[]{p.getId_servico(),p.getDesc(),p.getTempo(),p.getValor(),p.getId_funcionario_fk()});
        }
    }
    
    public void gerarTabela_com_Consulta(){
        DefaultTableModel modelo = (DefaultTableModel) tabelaServicos.getModel();
        modelo.setNumRows(0);
        ServicoDAO pDAO = new ServicoDAO();
        List<Servico> Lista = pDAO.listarTodos();
        for(Servico p: Lista){     
            if((p.getDesc().toLowerCase()).contains(edPesquisa.getText().toLowerCase())){
                modelo.addRow(new Object[]{p.getId_servico(),p.getDesc(),p.getTempo(),p.getValor(),p.getId_funcionario_fk()});
            }
        }
    }
    
    public void limparCampos(){
        edValor.setText(null);
        edTempo.setText(null);
        edDesc.setText(null);
    }
    
    public String alterarData(String data){
        String dia = data.substring(0, 1);
        String mes = data.substring(3, 4);
        String ano = data.substring(6, 9);
        String dataSQL = ano+"-"+mes+"-"+dia;
        return dataSQL;
    }
    
    public void trocarModo(Servico p){
        if(mode == 0){
            mode = 1;
            labelTitulo.setText("Editar Servico");
            btCadastrar.setText("Editar");
            labelId.setText("Id:"+p.getId_servico());
            //Pegando valores dos EDs
            edValor.setText(String.valueOf(p.getValor()));
            edTempo.setText(String.valueOf(p.getTempo()));
            edDesc.setText(p.getDesc());
        }else{
            mode = 0;
            labelTitulo.setText("Cadastrar Servico");
            btCadastrar.setText("Cadastrar");
            labelId.setText(null);
        }
    }
    
