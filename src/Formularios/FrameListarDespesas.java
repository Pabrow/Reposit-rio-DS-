    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        int opcao = li_pagar.getSelectedRow();
        if (opcao >= 0) {
            Despesa c = new Despesa();
            c.setId_despesa(Integer.parseInt(li_pagar.getValueAt(opcao, 0).toString()));
            DespesaDAO cdao = new DespesaDAO();
            cdao.deletar(c);
            preencherTabela();
        }
    }//GEN-LAST:event_btExcluirActionPerformed

    public void gerarTabelaDespesas() {
        DefaultTableModel modelo = (DefaultTableModel) li_pagar.getModel();
        modelo.setNumRows(0);
        DespesaDAO pDAO = new DespesaDAO();
        List<Despesa> Lista = pDAO.listarTodos();
        for (Despesa p : Lista) {
            modelo.addRow(new Object[]{p.getId_despesa(), p.getValor(), p.getData(), p.getFormaPag(), p.getDesc()});
        }
    }

    public void gerarTabelaDespesas_com_Consulta() {
        DefaultTableModel modelo = (DefaultTableModel) li_pagar.getModel();
        modelo.setNumRows(0);
        DespesaDAO pDAO = new DespesaDAO();
        List<Despesa> Lista = pDAO.listarTodos();
        for (Despesa p : Lista) {
            if (((p.getData().toLowerCase()).contains(edID.getText().toLowerCase()))) {
                modelo.addRow(new Object[]{p.getId_despesa(), p.getValor(), p.getData(), p.getFormaPag(), p.getDesc()});
            }
        }
    }
    
    public void preencherTabela() {
        DespesaDAO cdao = new DespesaDAO();
        List<Despesa> lista = cdao.listarTodos();
        DefaultTableModel tabela = (DefaultTableModel) li_pagar.getModel();
        tabela.setRowCount(0);
        for (Despesa c : lista) {
            tabela.addRow(new Object[]{c.getDesc(), c.getValor(),c.getData(), c.getFormaPag()});
        }
    }

