    private void btEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditarActionPerformed
        int opcao = li_pagar.getSelectedRow();
        if (opcao >= 0) {
            Despesa c = new Despesa();
            c.setDesc(li_pagar.getValueAt(opcao, 0).toString());
            c.setValor(Integer.parseInt(li_pagar.getValueAt(opcao, 1).toString()));
            c.setData(li_pagar.getValueAt(opcao, 2).toString());
            c.setFormaPag(li_pagar.getValueAt(opcao, 3).toString());
            DespesaDAO cdao = new DespesaDAO();
            cdao.editarPorID(c);
            preencherTabela();
    }//GEN-LAST:event_btEditarActionPerformed
    }
